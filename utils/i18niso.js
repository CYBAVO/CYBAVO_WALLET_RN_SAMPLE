/*
 * @param code Alpha-2 code
 * @return Alpha-3 code or undefined
 */
export function alpha2ToAlpha3(code, defaultValue) {
  let arr = isoCodes[code];
  if (arr) {
    return arr[1];
  }
  return defaultValue;
}
